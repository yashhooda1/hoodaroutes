// lib/garmin-official.js — OFFICIAL push via the Garmin Connect Developer
// Program (Courses API). All Developer Program APIs use OAuth 2.0 (Bearer).
//
// ⚠️ Access is approval-only and (as of 2026) paused for NEW sign-ups. Existing
// partners keep working. This is the production-grade, multi-user path: each
// user authorizes via OAuth 2.0, and you push courses to their account.
//
// The exact token + course URLs come with your partner onboarding, so they're
// env-configurable here. The course PAYLOAD already matches Garmin's published
// Courses-API schema.
//
// Env:
//   GARMIN_TOKEN_URL     OAuth2 token endpoint (from onboarding)
//   GARMIN_COURSES_URL   Courses API POST endpoint (from onboarding)
//   GARMIN_CLIENT_ID, GARMIN_CLIENT_SECRET
//   GARMIN_REFRESH_TOKEN (single-user) — or pass a per-user token in (multi-user)

export async function garminAccessToken(refreshToken = process.env.GARMIN_REFRESH_TOKEN) {
  const r = await fetch(process.env.GARMIN_TOKEN_URL, {
    method: "POST",
    headers: { "Content-Type": "application/x-www-form-urlencoded" },
    body: new URLSearchParams({
      grant_type: "refresh_token",
      refresh_token: refreshToken,
      client_id: process.env.GARMIN_CLIENT_ID,
      client_secret: process.env.GARMIN_CLIENT_SECRET,
    }),
  });
  if (!r.ok) throw new Error(`Garmin token ${r.status}: ${await r.text()}`);
  return (await r.json()).access_token;
}

export async function pushCourseOfficial(course, refreshToken) {
  const token = await garminAccessToken(refreshToken);
  const r = await fetch(process.env.GARMIN_COURSES_URL, {
    method: "POST",
    headers: { Authorization: `Bearer ${token}`, "Content-Type": "application/json" },
    body: JSON.stringify(course),
  });
  if (!r.ok) throw new Error(`Courses API ${r.status}: ${await r.text()}`);
  return await r.json();
}

// Build the verified Courses-API course object from generated route geometry.
export function buildCourse({ name, distanceM, ascentM, descentM, coordinates, activityType = "RUNNING" }) {
  return {
    courseName: name,
    description: "Generated by HoodaRoutes",
    distance: distanceM,
    elevationGain: ascentM,
    elevationLoss: descentM,
    coordinateSystem: "WGS84",
    activityType, // RUNNING | TRAIL_RUNNING | ...
    geoPoints: coordinates.map((c) => ({
      latitude: c[1],
      longitude: c[0],
      elevation: c[2] || 0,
    })),
  };
}
